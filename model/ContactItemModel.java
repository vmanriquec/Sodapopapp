package com.food.sistemas.sodapopapp.model;

public class ContactItemModel {

    String IdContacto, Telefono, Nombre, EmailFriend, CodContacto, IdUsuario;

    public String getIdContacto() {
        return IdContacto;
    }

    public void setIdContacto(String idContacto) {
        IdContacto = idContacto;
    }

    public String getTelefono() {
        return Telefono;
    }

    public void setTelefono(String telefono) {
        Telefono = telefono;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }

    public String getEmailFriend() {
        return EmailFriend;
    }

    public void setEmailFriend(String emailFriend) {
        EmailFriend = emailFriend;
    }

    public String getCodContacto() {
        return CodContacto;
    }

    public void setCodContacto(String codContacto) {
        CodContacto = codContacto;
    }

    public String getIdUsuario() {
        return IdUsuario;
    }

    public void setIdUsuario(String idUsuario) {
        IdUsuario = idUsuario;
    }
}
